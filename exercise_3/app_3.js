// Name Cases 3: Store a person’s name in a variable, and then print that person’s name in lowercase, uppercase, and titlecase.
//lower case
var person_name = "Muhammad";
console.log("lowercase:", person_name.toLowerCase());
//Upper case
console.log("uppercase:", person_name.toUpperCase());
//titrle case
console.log("titlecase:", person_name.charAt(0).toUpperCase() + person_name.slice(1));
